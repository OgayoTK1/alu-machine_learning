"""Module that provides a function for concatenating two arrays.
"""


def cat_arrays(arr1, arr2):
    """Concatenates two arrays.

    Args:
        arr1: List of ints/floats.
        arr2: List of ints/floats.

    Returns:
        A new list containing all elements of arr1 followed by all elements
        of arr2.
    """
    return arr1 + arr2
